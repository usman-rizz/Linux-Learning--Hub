/	    # Root directory    top of the tree	cd /

/home	# User home directories	/home/ubuntu, /home/hadoop

/etc	# Configuration files 	/etc/crontab, /etc/hosts

/var	# Variable data — logs, databases	/var/log/syslog

/tmp	# Temporary files — cleared on reboot	Store temp pipeline files here

/opt	# Optional/third-party software	/opt/spark, /opt/hadoop

/usr	# User programs and utilities	/usr/bin, /usr/local

/bin	# Essential command binaries	ls, cp, mv, cat are here

/sbin	# System binaries (admin use)	ifconfig, fdisk, mount

/proc	# Virtual FS — live system info	cat /proc/cpuinfo

/dev	# Device files	/dev/sda (hard disk)

/mnt	# Mount points for disks/NFS	/mnt/data-volume

/lib	# Shared libraries	Similar to Windows DLLs
