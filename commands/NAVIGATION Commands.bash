# These are the commands you type FIRST when you open a terminal. They tell you WHERE you are and let you MOVE around the file system.

pwd		    # Print Working Directory - shows your current location in the file system

ls		    # List files and directories in the current directory

ls -l		# List with details (permissions, owner, size, date)

ls -la       # List all files (including hidden) with details

ls -lh	    # Shows file sizes in KB/MB instead of bytes

ls -a		# List all files, including hidden ones (those starting with .)

cd ..		# Change Directory - move up one level in the directory tree

cd /		# Move to the root directory

cd ~		# Move to your home directory

cd -		# Move to the previous directory you were in

cd /path/to/directory	# Move to a specific directory (replace with actual path)
